"""

Даны 2 числа a и b. Получить их сумму, разность и произведение

"""


def find_sum(a, b):
    return a + b


def find_subtraction(a, b):
    return a - b


def find_multiplication(a, b):
    return a * b


print('The result of sum is ', (find_sum(3, 2)))
print('The result of subtraction is ', (find_subtraction(10, 2)))
print('The result of sum is multiplication ', (find_multiplication(3, 10)))
